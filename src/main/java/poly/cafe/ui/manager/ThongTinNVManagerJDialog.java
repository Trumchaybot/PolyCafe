    private void fillComboBoxRole() {
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        roleList = new RoleDAOImpl().findAll(); // Lưu danh sách

        for (Role role : roleList) {
            model.addElement(role.getRoleName()); // chỉ hiển thị tên
        }

        cboRole.setModel(model);
    }

    private void setSelectedRole(int roleId) {
        for (int i = 0; i < roleList.size(); i++) {
            if (roleList.get(i).getRoleId() == roleId) {
                cboRole.setSelectedIndex(i);
                break;
            }
        }
    }

