    @Override
    public void setForm(Object entity) {
        EmployeeInfo nv = (EmployeeInfo) entity;

        txtUserName.setText(nv.getUserName());
        txtPassWord.setText(nv.getPassWord());
        txtHoTen.setText(nv.getFullName());
        txtSDT.setText(nv.getPhoneNumber());
        txtEmail.setText(nv.getEmail());
        txtCCCD.setText(nv.getNationalID());
        txtDiaChi.setText(nv.getAddress());

        if (nv.getBirthDate()!= null) {
            txtNgaySinh.setText(nv.getBirthDate().toString());
        } else {
            txtNgaySinh.setText("");
        }

        

        // Giới tính
        if (nv.getGender().equalsIgnoreCase("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

        // Trạng thái
        if (nv.getStatus().equalsIgnoreCase("Enabled")) {
            rdoOnl.setSelected(true);
        } else {
            rdoOff.setSelected(true);
        }

        for (int i = 0; i < roleList.size(); i++) {
            if (roleList.get(i).getRoleId() == nv.getRole().getRoleId()) {
                cboRole.setSelectedIndex(i);
                break;
            }
        }
        if (nv.getPhoto() != null && !nv.getPhoto().isEmpty()) {
            File file = new File("images", nv.getPhoto());
            lblImage.setToolTipText(file.getName());
            XIcon.setIcon(lblImage, file);
            lblImage.setText("");
        } else {
            lblImage.setIcon(null);
            lblImage.setText("Chọn hình ảnh");
            lblImage.setToolTipText(null);
        }

    }

    @Override
public EmployeeInfo getForm() {
    EmployeeInfo nv = new EmployeeInfo();

    // Xóa thông báo cũ
    jLabel8.setText("");
    jLabel16.setText("");
    jLabel17.setText("");
    jLabel18.setText("");
    jLabel19.setText("");

    nv.setUserName(txtUserName.getText().trim());
    nv.setPassWord(txtPassWord.getText().trim());
    nv.setFullName(txtHoTen.getText().trim());

    // --- Kiểm tra nhập đầy đủ ---
    if (nv.getUserName().isEmpty() || nv.getPassWord().isEmpty() || nv.getFullName().isEmpty()
        || txtNgaySinh.getText().trim().isEmpty()
        || txtSDT.getText().trim().isEmpty()
        || txtEmail.getText().trim().isEmpty()
        || txtCCCD.getText().trim().isEmpty()
        || txtDiaChi.getText().trim().isEmpty()) {

        jLabel19.setForeground(Color.RED);
        jLabel19.setText("Vui lòng nhập đầy đủ thông tin!");
        return null;
    }

    // --- Kiểm tra ngày sinh ---
    try {
        LocalDate localDate = LocalDate.parse(txtNgaySinh.getText().trim());
        nv.setBirthDate(java.sql.Date.valueOf(localDate));
    } catch (Exception e) {
        jLabel8.setForeground(Color.RED);
        jLabel8.setText("Ngày sinh không hợp lệ! (yyyy-MM-dd)");
        return null;
    }

    // --- Kiểm tra số điện thoại ---
    String sdt = txtSDT.getText().trim();
    if (!sdt.matches("^0\\d{9}$")) {
        jLabel17.setForeground(Color.RED);
        jLabel17.setText("SĐT không hợp lệ (10 số, bắt đầu bằng 0)");
        return null;
    }
    nv.setPhoneNumber(sdt);

    // --- Kiểm tra email ---
    String email = txtEmail.getText().trim();
    if (!email.matches("^[\\w.-]+@[\\w.-]+\\.[a-zA-Z]{2,6}$")) {
        jLabel18.setForeground(Color.RED);
        jLabel18.setText("Email không hợp lệ!");
        return null;
    }
    nv.setEmail(email);

    // --- Kiểm tra CCCD ---
    String cccd = txtCCCD.getText().trim();
    if (!cccd.matches("^\\d{12}$")) {
        jLabel16.setForeground(Color.RED);
        jLabel16.setText("CCCD phải đủ 12 số!");
        return null;
    }
    nv.setNationalID(cccd);

    nv.setAddress(txtDiaChi.getText().trim());
    nv.setGender(rdoNu.isSelected() ? "Nữ" : "Nam");
    nv.setStatus(rdoOnl.isSelected() ? "Enabled" : "Disabled");


    // --- Lấy role ---
    String selectedRoleName = (String) cboRole.getSelectedItem();
    Role selectedRole = null;

    for (Role role : roleList) {
        if (role.getRoleName().equals(selectedRoleName)) {
            selectedRole = role;
            break;
        }
    }

    if (selectedRole != null) {
        nv.setRole(selectedRole);
    } else {
        XDialog.alert("Vui lòng chọn vai trò!");
        return null;
    }

    String image = lblImage.getToolTipText();
    nv.setPhoto(image != null ? image : "");

    return nv;
}



