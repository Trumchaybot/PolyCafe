    @Override
public void fillToTable() {
    DefaultTableModel model = (DefaultTableModel) tblUser.getModel();
    model.setRowCount(0);

    UserDAO dao = new UserDAOImpl();
    List<Object[]> list = dao.findAllWithRole();

    for (Object[] row : list) {
        String trangThai = (Integer) row[2] == 1 ? "Hoạt động" : "Không hoạt động";
        model.addRow(new Object[]{
            row[0],  // UserName
            row[1],  // PassWord
            trangThai, // Enabled
            row[3],  // Role
            row[4]   // Photo
        });
    }
}


