package poly.cafe.entity;

public class Statistical {
    private int totalOrfer;
    private int totalImport;
    private int totalExport;
    private float totalRevenue;
    private float orderIncome;
    private float exportIncome;
    private float totalExpenses;
    private float salaryExpense;
    private float importExpense;

    public Statistical() {}

    public int getTotalOrfer() {
        return totalOrfer;
    }

    public void setTotalOrfer(int totalOrfer) {
        this.totalOrfer = totalOrfer;
    }

    public int getTotalImport() {
        return totalImport;
    }

    public void setTotalImport(int totalImport) {
        this.totalImport = totalImport;
    }

    public int getTotalExport() {
        return totalExport;
    }

    public void setTotalExport(int totalExport) {
        this.totalExport = totalExport;
    }

    public float getTotalRevenue() {
        return totalRevenue;
    }

    public void setTotalRevenue(float totalRevenue) {
        this.totalRevenue = totalRevenue;
    }

    public float getOrderIncome() {
        return orderIncome;
    }

    public void setOrderIncome(float orderIncome) {
        this.orderIncome = orderIncome;
    }

    public float getExportIncome() {
        return exportIncome;
    }

    public void setExportIncome(float exportIncome) {
        this.exportIncome = exportIncome;
    }

    public float getTotalExpenses() {
        return totalExpenses;
    }

    public void setTotalExpenses(float totalExpenses) {
        this.totalExpenses = totalExpenses;
    }

    public float getSalaryExpense() {
        return salaryExpense;
    }

    public void setSalaryExpense(float salaryExpense) {
        this.salaryExpense = salaryExpense;
    }

    public float getImportExpense() {
        return importExpense;
    }

    public void setImportExpense(float importExpense) {
        this.importExpense = importExpense;
    }

    
}
