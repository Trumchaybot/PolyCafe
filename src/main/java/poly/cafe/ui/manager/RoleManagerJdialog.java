    public void open(){
        this.fillToTable();
    }
    public void fillToTable() {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0);
    try {
        list = dao.findAll();
        for (Role r : list) {
            Object[] row = {
    r.getRoleId(),
    r.getRole(),
    r.getRoleName(),
    r.getHourlySalary()
};
model.addRow(row);

        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}

public void create() {
    try {
        Role r = getForm();
        dao.create(r); // không cần set ID
        fillToTable();
        reset();
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm mới thành công!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Thêm mới thất bại!\n" + e.getMessage());
    }
}

public void update() {
    try {
        Role r = getForm();
        dao.update(r);
        fillToTable();
        javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Cập nhật thất bại!\n" + e.getMessage());
    }
}

public void delete() {
    try {
        int id = Integer.parseInt(lblidRole.getText());
        dao.deleteById(id);
        fillToTable();
        reset();
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thành công!");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại!\n" + e.getMessage());
    }
}


public void reset() {
    lblidRole.setText("Tạo mới hoặc sửa thông tin.");
    cboRole.setSelectedIndex(0);
    jTextField1.setText("");
    jTextField2.setText("");
}

public Role getForm() {
    Role r = new Role();
    if (!lblidRole.getText().equals("Tạo mới hoặc sửa thông tin.")) { // nếu đã có id hiển thị
        r.setRoleId(Integer.parseInt(lblidRole.getText()));
    }
    r.setRole(cboRole.getSelectedItem().toString());
    r.setRoleName(jTextField1.getText().trim());
    r.setHourlySalary(Float.parseFloat(jTextField2.getText().trim()));
    return r;
}

public void setForm(Role r) {
    lblidRole.setText(String.valueOf(r.getRoleId()));
    cboRole.setSelectedItem(r.getRole()); 
    jTextField1.setText(r.getRoleName());
    jTextField2.setText(String.valueOf(r.getHourlySalary()));
}

public void edit() {
    row = jTable1.getSelectedRow();
    if (row >= 0) {
        int id = (Integer) jTable1.getValueAt(row, 0);
        Role r = dao.findById(id);
        if (r != null) {
            setForm(r);
        }
    }
}

