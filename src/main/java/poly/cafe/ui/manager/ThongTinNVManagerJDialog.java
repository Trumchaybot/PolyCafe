    @Override
public void delete() {
    String userName = txtUserName.getText().trim();

    if (userName.isEmpty()) {
        XDialog.alert(this, "Vui lòng nhập hoặc chọn tài khoản cần xóa!");
        return;
    }

    // Chặn xóa tài khoản đang đăng nhập
    if (XAuth.isCurrentUser(userName)) {
    XDialog.alert(this, "Không thể xóa tài khoản đang đăng nhập!");
    return;
}


    if (XDialog.confirm("Bạn có chắc muốn xóa tài khoản \"" + userName + "\" không?")) {
        dao.deleteById(userName);
        this.clear();
        XDialog.alert(this, "Xóa thành công!");
    }
}


